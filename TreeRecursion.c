#include<stdio.h>
void fun(int n) // T(n)
{
  if(n>0) // 1 unit
  {
    printf("%d",n); // 1 Unit
    fun(n-1);  // T(n-1)
    fun(n-1);  // T(n-1)
  }
}
int main()
{
  int n=3;
  fun(n);
}

